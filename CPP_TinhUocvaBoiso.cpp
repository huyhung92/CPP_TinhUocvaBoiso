#include <iostream>
using namespace std;
int main() {
    int n;
    cout << "tinh uoc so";
    cin >> n;
    for (int i = 1; i = n; i++) {
        if (n % i == 0);
            cout << "uoc la" << i << " " << endl;
    }
    return 0;
}


